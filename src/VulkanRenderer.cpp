    m_graphicsQueue = VK_NULL_HANDLE;
    m_presentQueue = VK_NULL_HANDLE;
    m_swapChain = VK_NULL_HANDLE;
    m_renderPass = VK_NULL_HANDLE;
    m_descriptorSetLayout = VK_NULL_HANDLE;
